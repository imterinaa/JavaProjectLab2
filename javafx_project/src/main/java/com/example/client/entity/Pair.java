package com.example.client.entity;

public class Pair {
    public int i;
    public String s;

    public Pair(int i, String s) {
        this.i = i;
        this.s = s;
    }

    public int getInt() {
        return i;
    }
    public String getI() {
        return Integer.toString(i);
    }

    public String getS() {
        return s;
    }

    public String getString() {
        return s;
    }
}